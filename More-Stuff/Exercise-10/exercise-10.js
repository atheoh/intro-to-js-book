// > let y = "5"
// > y++

/*
The above piece of code returns 5 to the console because when "++" is applied to a 
string, in this case the string "5", it gets coerced to the number 5 and then gets 
incremented to the value of 6. However, the return value of y is 5 since the post-
increment operator returns the original value of y.
*/